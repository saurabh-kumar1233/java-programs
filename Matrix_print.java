public class Matrix_print {
    public static void main(String args[])
    {
      
        int arr1[][]={{1,2,3,4,5},{7,9,8,5,6}};
        
        for (int i=0;i<=4;i++)
        {
            for (int j=0;j<=4;j++)
            {
                System.out.print(arr1[i][j]);
            }
           System.out.println();
        }
    }
}

